#include "misc.h"

BOOL load_npcap_dlls() {
    _TCHAR npcap_dir[512];
    if (!GetSystemDirectory(npcap_dir, 480)) {
        spdlog::critical("Error in GetSystemDirectory: {}", GetLastError());
        return FALSE;
    }
    _tcscat_s(npcap_dir, 512, _T("\\Npcap"));
    if (SetDllDirectory(npcap_dir) == 0) {
        spdlog::critical("Error in SetDllDirectory: {}", GetLastError());
        return FALSE;
    }
    return TRUE;
}